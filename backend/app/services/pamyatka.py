def _form_rows(o, ctx) -> list[tuple[str, str]]:
    rows = [("Заказ", f"{ctx.no} · {o['work_label'] or '—'}")]
    if o["topic"]:
        t = o["topic"].strip()
        rows.append(("Тема", t if len(t) <= 110 else t[:107] + "…"))
    rows.append(("Передано", ctx.today))
    rows.append(("Окно проверки", f"7 дней — до {ctx.d_check}"))
    if ctx.d_serv:
        rows.append(("Сервисное окно", f"замечания вуза — ориентир до {ctx.d_serv}, "
                                       "продлевается кнопкой «Ещё жду проверок»"))
    if ctx.parts_line:
        rows.append(("Выдача", ctx.parts_line))
    return rows


