def _mk_pdf(doc_label: str):
    """Пустой лист с фирменным подвалом; None — если fpdf2/шрифты недоступны."""
    try:
        from fpdf import FPDF
        from fpdf.enums import XPos, YPos
    except Exception as e:  # noqa: BLE001
        log.warning("fpdf2 недоступен: %s", e)
        return None
    reg = _first_existing(_FONT_REGULAR)
    bold = _first_existing(_FONT_BOLD) or reg
    if not reg:
        log.warning("DejaVuSans.ttf не найден — памятка не сформирована")
        return None

    class PamPDF(FPDF):
        def footer(self):  # noqa: N802 — API fpdf2
            self.set_y(-14)
            self.set_draw_color(*HAIR)
            self.set_line_width(0.2)
            self.line(self.l_margin, self.get_y(), self.w - self.r_margin, self.get_y())
            self.set_font("DV", "", 7.4)
            self.set_text_color(*MUTE)
            self.cell(0, 8, f"{doc_label} · Академический Салон · {SITE}"
                            f"  —  страница {self.page_no()} из {{nb}}", align="C")

    pdf = PamPDF(format="A4")
    pdf.alias_nb_pages()
    pdf.set_margins(18, 16, 18)
    pdf.set_auto_page_break(auto=True, margin=20)
    pdf.add_font("DV", "", reg)
    pdf.add_font("DV", "B", bold)
    pdf.add_page()

    W = pdf.w - pdf.l_margin - pdf.r_margin
    HOME = dict(new_x=XPos.LMARGIN, new_y=YPos.NEXT)

    def sp(t: str) -> str:  # разрядка шапки: А К А Д Е М И Ч Е С К И Й
        return " ".join(t)

    def brand_head(series: str, title: str, sub: str = "") -> None:
        """Фирменная шапка первой страницы: набор, линейка, титул, печать."""
        pdf.set_font("DV", "B", 10.2)
        pdf.set_text_color(*WAX)
        pdf.cell(W, 6, sp("АКАДЕМИЧЕСКИЙ САЛОН"), align="C", **HOME)
        pdf.ln(1.2)
        pdf.set_font("DV", "", 8.2)
        pdf.set_text_color(*MUTE)
        pdf.cell(W, 4.6, series.upper(), align="C", **HOME)
        y = pdf.get_y() + 2.2
        pdf.set_draw_color(*HAIR)
        pdf.set_line_width(0.3)
        pdf.line(pdf.l_margin + W / 2 - 11, y, pdf.l_margin + W / 2 + 11, y)
        pdf.ln(6.5)
        pdf.set_font("DV", "B", 19)
        pdf.set_text_color(*INK)
        pdf.multi_cell(W, 8.6, title, align="C", **HOME)
        if sub:
            pdf.ln(0.6)
            pdf.set_font("DV", "", 10.4)
            pdf.set_text_color(*MUTE)
            pdf.multi_cell(W, 5.4, sub, align="C", **HOME)
        # сургучная печать в правом верхнем углу листа
        y_text = pdf.get_y()  # cell печати сдвинет курсор — запомним низ титула
        d = 15.5
        x, y0 = pdf.w - pdf.r_margin - d, 12.5
        pdf.set_fill_color(*WAX)
        pdf.set_draw_color(*WAX_DEEP)
        pdf.set_line_width(0.4)
        pdf.ellipse(x, y0, d, d, style="DF")
        pdf.set_draw_color(255, 244, 233)
        pdf.set_line_width(0.28)
        pdf.ellipse(x + 1.7, y0 + 1.7, d - 3.4, d - 3.4, style="D")
        pdf.set_xy(x, y0 + d / 2 - 2.6)
        pdf.set_font("DV", "B", 8.6)
        pdf.set_text_color(255, 244, 233)
        pdf.cell(d, 5.2, "АС", align="C")
        pdf.set_xy(pdf.l_margin, max(y_text, y0 + d) + 4)

    _sec_n = {"n": 0}

    def sec(title: str) -> None:
        """— 01 — ЗАГОЛОВОК РАЗДЕЛА (не отрывается от текста)."""
        _sec_n["n"] += 1
        if pdf.get_y() > pdf.h - 46:
            pdf.add_page()
        pdf.ln(4.2)
        pdf.set_font("DV", "B", 8.6)
        pdf.set_text_color(*WAX)
        pdf.cell(W, 4.6, f"—  {_sec_n['n']:02d}  —", align="L", **HOME)
        pdf.set_font("DV", "B", 12.6)
        pdf.set_text_color(*INK)
        pdf.multi_cell(W, 6.2, title, **HOME)
        y = pdf.get_y() + 1.4
        pdf.set_draw_color(*HAIR)
        pdf.set_line_width(0.25)
        pdf.line(pdf.l_margin, y, pdf.l_margin + W, y)
        pdf.ln(3.4)

    def sub(title: str) -> None:
        if pdf.get_y() > pdf.h - 36:
            pdf.add_page()
        pdf.ln(1.6)
        pdf.set_font("DV", "B", 10.2)
        pdf.set_text_color(*INK)
        pdf.multi_cell(W, 5.4, title, **HOME)
        pdf.ln(0.6)

    def p(t: str, size: float = 9.6, color=INK) -> None:
        pdf.set_font("DV", "", size)
        pdf.set_text_color(*color)
        pdf.multi_cell(W, 5.0, t, **HOME)
        pdf.ln(1.1)

    def mute(t: str) -> None:
        p(t, size=8.6, color=MUTE)

    def b(t: str) -> None:
        """Тезис с длинным тире."""
        pdf.set_font("DV", "", 9.6)
        pdf.set_text_color(*INK)
        x = pdf.l_margin
        pdf.set_x(x)
        pdf.cell(6, 5.0, "—")
        pdf.multi_cell(W - 6, 5.0, t, new_x="LMARGIN", new_y="NEXT")
        pdf.ln(0.7)

    def chk(t: str) -> None:
        """Пункт чек-листа с пустым квадратом (можно отмечать ручкой)."""
        if pdf.get_y() > pdf.h - 26:
            pdf.add_page()
        y = pdf.get_y()
        pdf.set_draw_color(*MUTE)
        pdf.set_line_width(0.32)
        pdf.rect(pdf.l_margin + 0.4, y + 1.0, 3.1, 3.1)
        pdf.set_x(pdf.l_margin + 6.4)
        pdf.set_font("DV", "", 9.6)
        pdf.set_text_color(*INK)
        pdf.multi_cell(W - 6.4, 5.0, t, new_x="LMARGIN", new_y="NEXT")
        pdf.ln(0.8)

    def tip(t: str, label: str = "Совет мастерской") -> None:
        """Кремовая врезка с сургучным корешком."""
        if pdf.get_y() > pdf.h - 40:
            pdf.add_page()
        x, y0 = pdf.l_margin, pdf.get_y() + 1
        pdf.set_xy(x + 4.6, y0 + 2.4)
        pdf.set_font("DV", "B", 8.2)
        pdf.set_text_color(*WAX)
        pdf.multi_cell(W - 8, 4.2, label.upper(), new_x="LMARGIN", new_y="NEXT")
        pdf.set_x(x + 4.6)
        pdf.set_font("DV", "", 9.2)
        pdf.set_text_color(*INK)
        pdf.multi_cell(W - 8, 4.8, t, new_x="LMARGIN", new_y="NEXT")
        y1 = pdf.get_y() + 2.2
        pdf.set_fill_color(*CREAM)
        pdf.rect(x, y0, W, y1 - y0, style="F")
        pdf.set_fill_color(*WAX)
        pdf.rect(x, y0, 1.1, y1 - y0, style="F")
        # текст рисовали до подложки — перерисуем поверх
        pdf.set_xy(x + 4.6, y0 + 2.4)
        pdf.set_font("DV", "B", 8.2)
        pdf.set_text_color(*WAX)
        pdf.multi_cell(W - 8, 4.2, label.upper(), new_x="LMARGIN", new_y="NEXT")
        pdf.set_x(x + 4.6)
        pdf.set_font("DV", "", 9.2)
        pdf.set_text_color(*INK)
        pdf.multi_cell(W - 8, 4.8, t, new_x="LMARGIN", new_y="NEXT")
        pdf.set_y(y1 + 2.4)

    def kv(rows: list[tuple[str, str]]) -> None:
        """Формуляр: подпись слева, значение справа, волосяные линейки."""
        for label, val in rows:
            if not val:
                continue
            y = pdf.get_y()
            pdf.set_font("DV", "", 8.4)
            pdf.set_text_color(*MUTE)
            pdf.cell(44, 5.6, label)
            pdf.set_font("DV", "", 9.6)
            pdf.set_text_color(*INK)
            pdf.multi_cell(W - 44, 5.6, val, new_x="LMARGIN", new_y="NEXT")
            pdf.set_draw_color(*PAPER_EDGE)
            pdf.set_line_width(0.2)
            pdf.line(pdf.l_margin, pdf.get_y() + 0.3, pdf.l_margin + W, pdf.get_y() + 0.3)
            pdf.ln(1.6)
        pdf.ln(1.2)

    def out() -> bytes:
        raw = pdf.output()
        return bytes(raw)

    return SimpleNamespace(pdf=pdf, W=W, brand_head=brand_head, sec=sec, sub=sub,
                           p=p, mute=mute, b=b, chk=chk, tip=tip, kv=kv, out=out)


