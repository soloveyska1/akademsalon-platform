def _defense_prep_block(H, short: bool = False) -> None:
    H.sec("Подготовка к защите: план на последние две недели")
    H.chk("Речь на 5–7 минут: актуальность (2 фразы) → цель и задачи → как исследовали → "
          "2–3 главных результата → выводы и практическая польза. Не пересказ работы, а витрина.")
    H.chk("Презентация 10–12 слайдов: титул, актуальность, цель/задачи, методика, результаты "
          "(графики вместо текста), выводы. Один слайд — одна мысль.")
    H.chk("Репетиция вслух с таймером минимум 3 раза; первые две фразы доклада — наизусть, "
          "они снимают ступор.")
    H.chk("Раздаточный материал/нормоконтроль — по требованиям кафедры, заранее, не в ночь.")
    if not short:
        H.sub("Вопросы комиссии: топ, который звучит почти всегда")
