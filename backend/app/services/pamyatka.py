async def build_order_pdf(o) -> bytes | None:
    """Персональная памятка к выдаче. None — если жанр не положен/нет fpdf."""
    fam = family_for(o)
    if not fam:
        return None
    no = config.order_no(o["id"])
    H = _mk_pdf(f"Памятка к заказу {no}")
    if not H:
        return None
    now = datetime.now(config.MSK)
    total = o["stages_total"] or 1
    parts_line = ""
    if total > 1:
        parts_line = f"по частям, финальная часть {total} из {total} передана"
    ctx = SimpleNamespace(
        no=no,
        today=now.strftime("%d.%m.%Y"),
        d_check=(now + timedelta(days=7)).strftime("%d.%m.%Y"),
        d_serv=((now + timedelta(days=60)).strftime("%d.%m.%Y")
                if fam in ("vkr", "course", "practice", "article", "defense") else None),
        parts_line=parts_line,
    )
    series, title, sub = _head_for(o, fam, ctx)
    H.brand_head(series, title, sub)
    _RENDERERS[fam](H, o, ctx)
    # колофон
    H.pdf.ln(4)
    H.mute("Составлено автоматически под ваш заказ. Юридические условия — в оферте "
           f"({SITE}/oferta.html) и спецификации заказа (PDF в кабинете); при расхождении "
           "формулировок действуют они. Материалы носят консультационно-методический "
           "характер и передаются как образец.")
    try:
        return H.out()
    except Exception as e:  # noqa: BLE001
        log.warning("pamyatka build failed for %s: %s", o["id"], e)
        return None


def order_pamyatka_ready(o) -> bool:
    """Финальная часть передана клиенту — памятке есть о чём говорить."""
    if family_for(o) is None:
        return False
    st = o["status"]
    if st == "done":
        return True
    total = o["stages_total"] or 1
    return st in ("check", "fix") and (o["stage"] or 1) >= total


